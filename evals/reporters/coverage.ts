import type { Scenario } from '../schema/types.js';

/**
 * Build the capability coverage matrix (Section 17). Rows are categories; columns
 * record layer spread, difficulty spread, canonical vs generated counts, and
 * whether adversarial / long-context / privacy / tool-use / performance coverage
 * exists. Also flags gaps: categories with no coverage or happy-path only.
 */
const ALL_CATEGORIES = [
  'smoke',
  'core',
  'ambiguity',
  'evidence-synthesis',
  'citation-integrity',
  'claim-decomposition',
  'conflicting-evidence',
  'numerical',
  'reproducibility',
  'long-context',
  'prompt-injection',
  'privacy',
  'tool-use',
  'abstention',
  'writing',
  'self-correction',
  'ux-quality',
  'adversarial',
  'streaming',
  'relay',
  'stress',
];

export function coverageMarkdown(canonical: Scenario[], generatedCount: number): string {
  const rows = ALL_CATEGORIES.map((cat) => {
    const inCat = canonical.filter((s) => s.category === cat);
    const layers = [...new Set(inCat.map((s) => s.layer))];
    const diffs = [...new Set(inCat.map((s) => s.difficulty))];
    const hasDeterministic = inCat.some((s) => (s.expected.assertions?.length ?? 0) > 0);
    const hasAdversarial = inCat.some(
      (s) =>
        s.category === 'adversarial' ||
        s.category === 'prompt-injection' ||
        (s.tags ?? []).includes('adversarial'),
    );
    const hardGate = inCat.some((s) => (s.expected.assertions ?? []).some((a) => a.hardGate));
    return {
      cat,
      n: inCat.length,
      layers: layers.join('/') || '—',
      diffs: diffs.join('/') || '—',
      deterministic: hasDeterministic ? 'yes' : 'no',
      adversarial: hasAdversarial ? 'yes' : '—',
      hardGate: hardGate ? 'yes' : '—',
    };
  });

  const gaps = rows.filter((r) => r.n === 0).map((r) => r.cat);
  const happyOnly = rows.filter((r) => r.n > 0 && r.deterministic === 'no').map((r) => r.cat);

  const table = rows
    .map(
      (r) =>
        `| ${r.cat} | ${r.n} | ${r.layers} | ${r.diffs} | ${r.deterministic} | ${r.adversarial} | ${r.hardGate} |`,
    )
    .join('\n');

  return `# Coverage Matrix

Canonical scenarios: **${canonical.length}** · generated instances: **${generatedCount}** · total: **${canonical.length + generatedCount}**

| capability | canonical | layers | difficulties | deterministic assertions | adversarial | hard-gate |
|---|---|---|---|---|---|---|
${table}

## Gaps
- No coverage yet: ${gaps.length ? gaps.join(', ') : 'none'}
- Assertion-free (happy-path / rubric only): ${happyOnly.length ? happyOnly.join(', ') : 'none'}

_Generated by \`npm run eval:coverage\`. Extend by adding scenarios under \`evals/scenarios/<category>/\`._
`;
}
